
CREATE SCHEMA STUDENTS
CREATE TABLE STUDENTS.DETAILS(
ID INT PRIMARY KEY NOT NULL,
FIRST_NAME VARCHAR(50) NOT NULL,
LAST_NAME VARCHAR(50) NOT NULL,
D_O_b DATE NOT NULL,
MATRIC INT NOT NULL,
DEPARTMENT VARCHAR(50) NOT NULL,
)
select * from STUDENTS.DETAILS
SELECT * FROM STUDENTS.LIBRARYCARD_DETAILS
SELECT * FROM STUDENTS.BORROWERSCARD
SELECT* FROM STUDENTS.LIBRARYPAYMENT
SELECT * FROM STUDENTS.LIBRARYUSAGE

CREATE TABLE STUDENTS.LIBRARYCARD_DETAILS(
ID INT PRIMARY KEY NOT NULL,
CARDNUMBER INT  NOT NULL,
DATE_COLLECTED DATE NOT NULL,
YEAR_OF_VALIDITY INT NOT NULL,
)
SELECT * FROM STUDENTS.LIBRARYCARD_DETAILS

CREATE TABLE STUDENTS.BORROWERSCARD(
ID INT PRIMARY KEY NOT NULL,
DATE_of_COLLECTION DATE NOT NULL,
NUMBER_OF_BORROWERS_CARD TINYINT NOT NULL,
YEARS_OF_VALIDITY TINYINT NOT NULL,
)

CREATE TABLE STUDENTS.LIBRARYPAYMENT(
ID INT PRIMARY KEY NOT NULL,
DATE_OF_FIRST_PAYMENT DATE NOT NULL,
AMOUNT_PAID INT NOT NULL,
DATE_OF_SECOND_PAYMENT DATE NOT NULL,
AMOUNT_BALANCED INT NOT NULL,
)

CREATE TABLE STUDENTS.LIBRARYUSAGE (
ID INT PRIMARY KEY NOT NULL,
AMOUNT_OF_USAGE TINYINT NOT NULL,
DATE_LAST_USED DATE NOT NULL)

